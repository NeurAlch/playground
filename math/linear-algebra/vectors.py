# An array of numbers.
# Numbers are arranged in order.
# We can identify each individual number by its index in that ordering.
# Typically we give vectors lowercase names in bold typeface.
# When we need to explicitly identify the elements of a vector,
# we write them as a column enclosed in square brackets.
# We can think of vectors as identifying points in space

# Can be though of as matrices that contain only one column.
